    private void jCheckoutRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckoutRoomActionPerformed
        // TODO add your handling code here:
        int row = this.jBookingTable.getSelectedRow();
        String id_booking_selected = (this.jBookingTable.getModel().getValueAt(row, 0).toString());
        String name_room = (this.jBookingTable.getModel().getValueAt(row, 2).toString());
        bookingDAO.checkoutRoom(id_booking_selected, name_room);
        dispose();
        DanhSachDatPhong danhSachDatPhong = new DanhSachDatPhong();
        danhSachDatPhong.setVisible(true);
        
    }//GEN-LAST:event_jCheckoutRoomActionPerformed

