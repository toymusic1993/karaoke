    public DanhSachDatPhong() {
        initComponents();
        ArrayList<Booking> list_booking = bookingDAO.get_list_booking();
        DefaultTableModel list_booking_table = (DefaultTableModel) jBookingTable.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < list_booking.size(); i++) {
            row[0] = list_booking.get(i).getId();
            row[1] = clientDAO.get_name_client_by_id(list_booking.get(i).getClientId());
            row[2] = roomDAO.get_name_room_by_id(list_booking.get(i).getRoomId());
            row[3] = list_booking.get(i).getTimeStart();
            row[4] = list_booking.get(i).getTimeEnd();
            row[5] = list_booking.get(i).getDay();
            list_booking_table.addRow(row);
        }
        this.jCheckoutRoom.setEnabled(false);
        this.jDeleteBooking.setEnabled(false);
    }

